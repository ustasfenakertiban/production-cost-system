
#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏—è –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –∏–∑ –±—ç–∫–∞–ø–∞

BACKUP_DIR="/home/ubuntu/production_cost_system/backups"
ENV_FILE="/home/ubuntu/production_cost_system/app/.env"

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

echo -e "${GREEN}üîÑ –í–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –∏–∑ –±—ç–∫–∞–ø–∞${NC}"
echo "=============================================="
echo ""

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –±—ç–∫–∞–ø–æ–≤
if [ ! -d "$BACKUP_DIR" ] || [ -z "$(ls -A $BACKUP_DIR/backup_*.sql 2>/dev/null)" ]; then
    echo -e "${RED}‚ùå –ë—ç–∫–∞–ø—ã –Ω–µ –Ω–∞–π–¥–µ–Ω—ã –≤ $BACKUP_DIR${NC}"
    exit 1
fi

# –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Å–ø–∏—Å–æ–∫ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö –±—ç–∫–∞–ø–æ–≤
echo "üìÅ –î–æ—Å—Ç—É–ø–Ω—ã–µ –±—ç–∫–∞–ø—ã:"
echo ""
ls -lht "$BACKUP_DIR"/backup_*.sql | awk '{printf "%2d) %s  %s  %s %s %s\n", NR, $9, $5, $6, $7, $8}'
echo ""

# –ü–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –±—ç–∫–∞–ø–æ–≤
BACKUP_COUNT=$(ls -1 "$BACKUP_DIR"/backup_*.sql | wc -l)

# –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –≤—ã–±–æ—Ä –±—ç–∫–∞–ø–∞
echo -n "–í—ã–±–µ—Ä–∏—Ç–µ –Ω–æ–º–µ—Ä –±—ç–∫–∞–ø–∞ –¥–ª—è –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏—è (1-$BACKUP_COUNT) –∏–ª–∏ 'q' –¥–ª—è –≤—ã—Ö–æ–¥–∞: "
read CHOICE

if [ "$CHOICE" = "q" ] || [ "$CHOICE" = "Q" ]; then
    echo "–û—Ç–º–µ–Ω–µ–Ω–æ."
    exit 0
fi

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç—å –≤—ã–±–æ—Ä–∞
if ! [[ "$CHOICE" =~ ^[0-9]+$ ]] || [ "$CHOICE" -lt 1 ] || [ "$CHOICE" -gt "$BACKUP_COUNT" ]; then
    echo -e "${RED}‚ùå –ù–µ–≤–µ—Ä–Ω—ã–π –≤—ã–±–æ—Ä!${NC}"
    exit 1
fi

# –ü–æ–ª—É—á–∞–µ–º –ø—É—Ç—å –∫ –≤—ã–±—Ä–∞–Ω–Ω–æ–º—É –±—ç–∫–∞–ø—É
BACKUP_FILE=$(ls -1t "$BACKUP_DIR"/backup_*.sql | sed -n "${CHOICE}p")

echo ""
echo -e "${YELLOW}‚ö†Ô∏è  –í–ù–ò–ú–ê–ù–ò–ï!${NC}"
echo "–ë—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω–µ–Ω–æ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ –∏–∑ —Ñ–∞–π–ª–∞:"
echo "  $BACKUP_FILE"
echo ""
echo "–≠—Ç–æ —É–¥–∞–ª–∏—Ç –í–°–ï —Ç–µ–∫—É—â–∏–µ –¥–∞–Ω–Ω—ã–µ –≤ –±–∞–∑–µ –∏ –∑–∞–º–µ–Ω–∏—Ç –∏—Ö –¥–∞–Ω–Ω—ã–º–∏ –∏–∑ –±—ç–∫–∞–ø–∞!"
echo ""
echo -n "–í—ã —É–≤–µ—Ä–µ–Ω—ã? –í–≤–µ–¥–∏—Ç–µ 'yes' –¥–ª—è –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è: "
read CONFIRMATION

if [ "$CONFIRMATION" != "yes" ]; then
    echo "–û—Ç–º–µ–Ω–µ–Ω–æ."
    exit 0
fi

# –ó–∞–≥—Ä—É–∂–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è
if [ -f "$ENV_FILE" ]; then
    export $(grep -v '^#' "$ENV_FILE" | xargs)
else
    echo -e "${RED}‚ùå –§–∞–π–ª .env –Ω–µ –Ω–∞–π–¥–µ–Ω: $ENV_FILE${NC}"
    exit 1
fi

# –°–æ–∑–¥–∞–µ–º –±—ç–∫–∞–ø –ø–µ—Ä–µ–¥ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ–º
echo ""
echo "üì¶ –°–æ–∑–¥–∞–Ω–∏–µ —Å—Ç—Ä–∞—Ö–æ–≤–æ—á–Ω–æ–≥–æ –±—ç–∫–∞–ø–∞ –ø–µ—Ä–µ–¥ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ–º..."
SAFETY_BACKUP="$BACKUP_DIR/before_restore_$(date +%Y%m%d_%H%M%S).sql"
pg_dump "$DATABASE_URL" > "$SAFETY_BACKUP" 2>&1

if [ $? -eq 0 ]; then
    echo -e "${GREEN}‚úÖ –°—Ç—Ä–∞—Ö–æ–≤–æ—á–Ω—ã–π –±—ç–∫–∞–ø —Å–æ–∑–¥–∞–Ω: $SAFETY_BACKUP${NC}"
else
    echo -e "${YELLOW}‚ö†Ô∏è  –ù–µ —É–¥–∞–ª–æ—Å—å —Å–æ–∑–¥–∞—Ç—å —Å—Ç—Ä–∞—Ö–æ–≤–æ—á–Ω—ã–π –±—ç–∫–∞–ø, –Ω–æ –ø—Ä–æ–¥–æ–ª–∂–∞–µ–º...${NC}"
fi

# –í–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ –∏–∑ –±—ç–∫–∞–ø–∞
echo ""
echo "üîÑ –í–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ –∏–∑ –±—ç–∫–∞–ø–∞..."
psql "$DATABASE_URL" < "$BACKUP_FILE" 2>&1 | grep -v "^SET" | grep -v "^--" | head -20

if [ $? -eq 0 ]; then
    echo ""
    echo -e "${GREEN}‚úÖ –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö —É—Å–ø–µ—à–Ω–æ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞!${NC}"
    echo ""
    echo "üìä –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è:"
    echo "  ‚Ä¢ –í–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ –∏–∑: $(basename $BACKUP_FILE)"
    echo "  ‚Ä¢ –°—Ç—Ä–∞—Ö–æ–≤–æ—á–Ω—ã–π –±—ç–∫–∞–ø: $(basename $SAFETY_BACKUP)"
    echo ""
    echo "üí° –ù–µ –∑–∞–±—É–¥—å—Ç–µ –ø–µ—Ä–µ–∑–∞–ø—É—Å—Ç–∏—Ç—å –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ:"
    echo "   cd /home/ubuntu/production_cost_system/app"
    echo "   yarn prisma generate"
else
    echo ""
    echo -e "${RED}‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–∏ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö!${NC}"
    echo ""
    echo "–ï—Å–ª–∏ –¥–∞–Ω–Ω—ã–µ –ø–æ–≤—Ä–µ–∂–¥–µ–Ω—ã, –≤—ã –º–æ–∂–µ—Ç–µ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–∏—Ç—å—Å—è –∏–∑ —Å—Ç—Ä–∞—Ö–æ–≤–æ—á–Ω–æ–≥–æ –±—ç–∫–∞–ø–∞:"
    echo "  psql \"\$DATABASE_URL\" < $SAFETY_BACKUP"
    exit 1
fi
